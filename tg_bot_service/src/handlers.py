async def new_command(update: Update, context: ContextTypes.DEFAULT_TYPE):
    """–û–±—Ä–∞–±–æ—Ç—á–∏–∫ –∫–æ–º–∞–Ω–¥—ã /new - —Å–±—Ä–æ—Å —Å–µ—Å—Å–∏–∏"""
    user = update.effective_user
    old_session_id = None
    
    # –ü–æ–ª—É—á–∞–µ–º —Å—Ç–∞—Ä—É—é —Å–µ—Å—Å–∏—é –¥–ª—è –ª–æ–≥–∏—Ä–æ–≤–∞–Ω–∏—è
    old_session = session_manager.get_session(user.id)
    if old_session:
        old_session_id = old_session.session_id
    
    # –°–æ–∑–¥–∞–µ–º –Ω–æ–≤—É—é —Å–µ—Å—Å–∏—é
    new_session = session_manager.create_new_session(user.id)
    
    message = f"""
üîÑ –°–µ—Å—Å–∏—è —Å–±—Ä–æ—à–µ–Ω–∞!

üìù –ù–æ–≤–∞—è —Å–µ—Å—Å–∏—è —Å–æ–∑–¥–∞–Ω–∞:
{new_session.session_id}

–°—Ç–∞—Ä–∞—è —Å–µ—Å—Å–∏—è: {old_session_id or '–Ω–µ –±—ã–ª–æ'}

–¢–µ–ø–µ—Ä—å –º–æ–∂–µ—Ç–µ –∑–∞–¥–∞–≤–∞—Ç—å –≤–æ–ø—Ä–æ—Å—ã –≤ –Ω–æ–≤–æ–º –∫–æ–Ω—Ç–µ–∫—Å—Ç–µ.
"""
    
    await update.message.reply_text(message)
    
    logger.info(f"User {user.id} reset session: {old_session_id} -> {new_session.session_id}")


